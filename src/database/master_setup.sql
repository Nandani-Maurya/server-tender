-- User Role Access Table
CREATE TABLE IF NOT EXISTS tender.user_role_access (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  role_id bigint NULL,
  updated_at timestamp with time zone NULL,
  user_id bigint NULL,
  CONSTRAINT user_roles_pkey PRIMARY KEY (id),
  CONSTRAINT user_role_access_user_id_fkey FOREIGN KEY (user_id) REFERENCES tender.users (id) ON UPDATE CASCADE ON DELETE CASCADE,
  CONSTRAINT user_roles_role_id_fkey FOREIGN KEY (role_id) REFERENCES tender.roles (id) ON UPDATE CASCADE ON DELETE CASCADE
);

-- Pages Table
CREATE TABLE IF NOT EXISTS tender.pages (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  page_name text NOT NULL,
  CONSTRAINT Pages_pkey PRIMARY KEY (id),
  CONSTRAINT pages_page_name_key UNIQUE (page_name)
);

-- User/Role Page Access Table
CREATE TABLE IF NOT EXISTS tender.user_page_access (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  user_id bigint NULL,
  role_id bigint NULL,
  page_id bigint NULL,
  updated_at timestamp with time zone NULL,
  can_create boolean NOT NULL DEFAULT false,
  can_view boolean NOT NULL DEFAULT false,
  can_edit boolean NOT NULL DEFAULT false,
  can_delete boolean NOT NULL DEFAULT false,
  CONSTRAINT user_page_access_pkey PRIMARY KEY (id),
  CONSTRAINT user_page_access_user_id_fkey FOREIGN KEY (user_id) REFERENCES tender.users (id) ON UPDATE CASCADE ON DELETE CASCADE,
  CONSTRAINT user_page_access_role_id_fkey FOREIGN KEY (role_id) REFERENCES tender.roles (id) ON UPDATE CASCADE ON DELETE CASCADE,
  CONSTRAINT user_page_access_page_id_fkey FOREIGN KEY (page_id) REFERENCES tender.pages (id) ON UPDATE CASCADE ON DELETE CASCADE
);

