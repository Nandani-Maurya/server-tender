-- Create Schema if not exists
CREATE SCHEMA IF NOT EXISTS tender;

-- Create Enum Type for Life Cycle Status (if not exists)
DO $$ 
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'life_cycle_status' AND typnamespace = 'tender'::regnamespace) THEN
        CREATE TYPE tender.life_cycle_status AS ENUM ('ACTIVE', 'INACTIVE', 'DELETED', 'BLOCKED');
    END IF;
END $$;

-- Roles Table Fixes
CREATE TABLE IF NOT EXISTS tender.roles (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  role_name text NOT NULL,
  updated_at timestamp with time zone NULL,
  life_cycle_status tender.life_cycle_status NOT NULL DEFAULT 'ACTIVE',
  CONSTRAINT Role_pkey PRIMARY KEY (id),
  CONSTRAINT Role_role_name_key UNIQUE (role_name)
);

-- Ensure life_cycle_status has default and is not null in roles
ALTER TABLE tender.roles ALTER COLUMN life_cycle_status SET DEFAULT 'ACTIVE';
UPDATE tender.roles SET life_cycle_status = 'ACTIVE' WHERE life_cycle_status IS NULL;
ALTER TABLE tender.roles ALTER COLUMN life_cycle_status SET NOT NULL;

-- Users Table Fixes
CREATE TABLE IF NOT EXISTS tender.users (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  name text NULL,
  email text NOT NULL,
  password text NOT NULL,
  phone_number text NULL,
  display_id text NOT NULL,
  updated_at timestamp with time zone NULL,
  life_cycle_status tender.life_cycle_status NOT NULL DEFAULT 'ACTIVE',
  CONSTRAINT User_pkey PRIMARY KEY (id),
  CONSTRAINT users_display_id_key UNIQUE (display_id),
  CONSTRAINT users_email_key UNIQUE (email)
);

-- Add missing columns to users
DO $$ 
BEGIN
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_schema='tender' AND table_name='users' AND column_name='password') THEN
        ALTER TABLE tender.users ADD COLUMN password text;
    END IF;
    
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_schema='tender' AND table_name='users' AND column_name='email') THEN
        ALTER TABLE tender.users ADD COLUMN email text;
    END IF;
END $$;

